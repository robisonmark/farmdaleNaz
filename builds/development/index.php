                    <h3>What Kind of Worship Style Can I Expect?</h3>
                        <p>At Farmdale we want to see generations of God's choosen people worshiping together, therefore we have a blended worship style. We utilize hymns and modern songs to create a worship experience that allows you to connect with God. For us worship is more than just the music we play but about the application, loving God and loving our neighbor.</p>
                    <h3>I Noticed You Don't Pass Offering Plates</h3>
                        <p>At Farmdale we believe that the giving of tithes and offerings is an act of worship. We believe that this act of worship is between you and God, therefore we have Joy boxes located in the back of the sanctuary. We don't want anyone to feel pressured into putting money "into the plate" because they think that everyone will see that they don't put anything in. We want you to feel like your are giving to God out of love and devotion to him and not out of shame and fear that someone else is watching!</p>
