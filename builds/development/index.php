                    <h3>Do You Have Anything for My Kids?</h3>
                        <p>Not only do we offer a Nursery for infants but we also have Bibleland during the Sunday School hour. In Bibleland our kids (from ages 3 -11) get to hear a Bible story then the rest of the month that story becomes a part of them through various means like making crafts and learning to cook. We also offer classes for our teens during the Sunday School hour (9:30-10:10). During the worship service our teens stay in the service and get to be a part of the worship. However, after the music, our children (ages 3-7) and pre-teens (8-11) get to be involved in children's church and junior church (respectively); giving them an experience that is more suited toward their attention spans. If you feel more comfortable having your kids in the worship service with you that is just fine. It is always a pleasure to see kids in Service!</p>

